    # 1. Відправляємо в основний канал @cem_copok
    print(f"📡 Відправка в канал {config.TARGET_CHANNEL}...")
    res_channel = await send_rich_message_http(config.BOT_TOKEN, config.TARGET_CHANNEL, GLOSSARY_HTML)
    print(f"Результат каналу: {json.dumps(res_channel, indent=2, ensure_ascii=False)}")
    
    # 2. Відправляємо в канал бібліотеки @l_ibrar_y
    # Використовуємо токен бібліотекаря, який має бути адміном у бібліотеці
